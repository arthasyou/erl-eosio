-module(u_md5).

%% ====================================================================
%% API functions
%% ====================================================================
-export([encode/1]).

encode(List) ->	
  Bin = list_to_binary([io_lib:format("~2.16.0b", [N]) || N <- binary_to_list(erlang:md5(List))]),
  binary_to_list(Bin).



